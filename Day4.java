public class Main {
	public static void main(String[] args) {
	    // \n untuk membuat paragraf baru
	    System.out.println("Farel \nInformatika");
	    // \t untuk tab
	    System.out.println("Farel\tInformatika");
	    // \b untuk backpase atau menghapus satu huruf ke belakang 
	    System.out.println("Fa\brel Informatika");
	    // \' untuk memberikan petik 1
	    System.out.println("Farel \'Informatika\'");
	    // \" untuk memberi petik 2
	    System.out.println("Farel \"Informatika\"");
	    // \\ untuk membuat atau menampilkan backslash
	    System.out.println("Farel\\Informatika");
	    
	} 
}
